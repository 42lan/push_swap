#include "push_swap.h"

void	three_sort(int *s_a)
{
	if (s_a[2] > s_a[1] && s_a[2] < s_a[0] && s_a[1] < s_a[0])
		sa();
	else if (s_a[2] > s_a[1] && s_a[2] > s_a[0] && s_a[1] > s_a[0])
	{
		sa();
		rra();
	}
	else if (s_a[2] < s_a[1] && s_a[2] < s_a[0] && s_a[1] > s_a[0])
	{
		sa();
		ra();
	}
	else if (s_a[2] > s_a[1] && s_a[2] > s_a[0] && s_a[1] < s_a[0])
		ra();
	else if (s_a[2] < s_a[1] && s_a[2] > s_a[0] && s_a[1] > s_a[0])
		rra();
}
